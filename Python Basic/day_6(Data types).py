a = 1  # int type
print('The number is : ', a)

b = "harry"  # string type
b1 = "code with "  # concatenation of two string
print('The name is  : ', b1 + b)

c = 1.2  # float type
print('The decimal number is : ', c)

d = "#"  # char type
print('The character is : ', d)

e = complex(1, 2)  # complex type
print('The value is : ', e)

f = [1, 2.3, [-4, 5], ["apple", "banana"]]  # list type
print('The list is  : ', f)

g = (("apple", "banana"), 2.2, 4)  # tuple type
print('The tuple is : ', g)

h = {"name": "Shashi", "age": 20, "canVote": True}  # dictionary type
print('The dictionary is : ', h)

i = bool(True)  # boolean type
print('The boolean value is : ', i)

print(type(a))  # type() to show the type of data
print(type(b))
print(type(c))
print(type(d))
print(type(e))
print(type(f))
print(type(g))
print(type(h))
print(type(i))

# Exersice-1 (Calculator)

num1 = float(input("Enter the first number : "))
num2 = float(input("Enter the second number : "))
print('Addition : ', num1 + num2)
print('Subtraction : ', num1 - num2)
print('Division : ', num1 / num2)
print('Multiplication : ', num1 * num2)
print('Floor division : ', num1 // num2)  # divide first with second and round it with the nearest whole number
print('Modulo division : ', num1 % num2)  # give reminder
print('Power : ', num1 ** num2)    # base and power
